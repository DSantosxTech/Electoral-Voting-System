 public void carregarTabela(JTable tboDados) {
        String sql = "SELECT tb02_cod_cad, tb02_nome, tb02_cod_chapa, tb03_descricao, tb02_votos\n"
                + "FROM tb02_candidatos\n"
                + "INNER JOIN tb03_chapa ON tb02_candidatos.tb02_cod_chapa = tb03_chapa.tb03_cod_chapa\n"
                + "ORDER BY tb02_votos DESC";

        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            DefaultTableModel tab = (DefaultTableModel) tboDados.getModel();
            tab.setRowCount(0); // Limpa a tabela antes de preenchÃª-la novamente
            while (rs.next()) {
                tab.addRow(new Object[]{rs.getInt("tb02_cod_cad"), rs.getString("tb02_nome"),
                        rs.getString("tb02_cod_chapa"), rs.getString("tb03_descricao"), rs.getString("tb02_votos")});
            }
        } catch (SQLException ex) {
            System.out.println("Erro: " + ex);
        }
    }
    
 

